/**
 * 새로운 콘텐츠 생성 지침 유틸리티 (prompt 1,2.txt 기반)
 * 정보성과 판매성 콘텐츠를 위한 전문 가이드라인
 */

/**
 * 콘텐츠 타입에 따른 제목 생성 가이드라인
 */
export function getTitleGuideline(contentType: string, keyword: string, subKeywords: string[]): string {
  if (contentType === 'informational') {
    return `
정보성 제목 생성 지침 (판매성 스타일 활용):
당신은 10년 경력의 콘텐츠 마케터이자 전문 분야 에디터입니다.

제목 패턴: **실패/문제 + 해결 암시**
- 형식: "[실패 상황] + [성공 가능성]"
- 메인 키워드 "${keyword}" 필수 포함
- 보조 키워드 중 1-2개 활용: ${subKeywords.join(', ')}

예시 패턴:
- "${keyword}를 제대로 못하면 돈만 날립니다"
- "90%가 모르는 ${keyword} 활용법"
- "${keyword} 실패하는 사람들의 5가지 공통점"
- "${keyword}, 이렇게 하면 누구나 성공합니다"

독자의 호기심과 불안감을 동시에 자극하는 제목을 만드세요.
`;
  } else {
    return `
판매성 제목 생성 지침:
당신은 15년 경력의 이커머스 마케팅 전문 카피라이터입니다.

제목 패턴: **감정 트리거 + 구체적 결과**
- 형식: "**[감정/상황]** + [구체적 변화/해결]"
- 메인 키워드 "${keyword}" 필수 포함
- 보조 키워드로 감정 호소: ${subKeywords.join(', ')}

예시 패턴:
- "**엄마를 위한 ${keyword}입니다**"
- "**당신이 아무리 노력해도 ${keyword} 안 되는 이유**"
- "**${keyword} 때문에 고민이시죠?**"
- "**드디어 찾았습니다. ${keyword} 완벽 해결책**"

타겟 고객의 감정과 고민을 직격하는 제목을 만드세요.
`;
  }
}

/**
 * 콘텐츠 타입에 따른 키워드 추천 가이드라인
 */
export function getKeywordGuideline(contentType: string, topic: string): string {
  if (contentType === 'informational') {
    return `
정보성 키워드 추천 지침:
메인 주제: ${topic}

당신은 10년 경력의 콘텐츠 마케터입니다.
타겟 독자의 니즈와 문제점을 파악하여 키워드를 선정하세요.

키워드 선정 기준:
1. 핵심 키워드: 메인 주제의 핵심 가치 (높은 검색량)
2. 보조 키워드 6개:
   - 문제/실패 키워드: 독자가 겪는 어려움 관련
   - 해결책 키워드: 구체적 방법론, 실행법 관련
   - 결과/성과 키워드: 기대 효과, 변화 관련
   - 타겟 키워드: 초보자, 중급자 등 독자층 관련
   - 도구/리소스 키워드: 필요한 도구, 플랫폼 관련
   - 전문성 키워드: 전문가, 고급 기법 관련

판매성 글의 구조를 활용한 정보성 키워드를 선정하세요.
`;
  } else {
    return `
판매성 키워드 추천 지침:
제품/서비스: ${topic}

당신은 15년 경력의 이커머스 마케팅 전문 카피라이터입니다.
고객의 감정과 구매 동기를 자극하는 키워드를 선정하세요.

키워드 선정 기준:
1. 핵심 키워드: 제품/서비스명 또는 핵심 기능
2. 보조 키워드 6개:
   - 고객 고민/문제: 타겟이 겪는 고통점, 불편함
   - 감정 호소: 가족, 건강, 성공, 성장 관련
   - 차별화 포인트: 독특함, 특허, 인증, 전문성
   - 효과/결과: 구체적 변화, 개선 효과
   - 사회적 증거: 판매량, 후기, 순위, 인기
   - 긴급성: 한정, 특가, 마감임박 등

고객의 구매 심리를 자극하는 감정적 키워드를 포함하세요.
`;
  }
}

/**
 * 콘텐츠 타입에 따른 본문 생성 가이드라인
 */
export function getContentGuideline(contentType: string, title: string, primaryKeyword: string, subKeywords: string[]): string {
  const baseInfo = `
제목: ${title}
핵심 키워드: ${primaryKeyword}
보조 키워드: ${subKeywords.join(', ')}

글자 수: 2000-3000자 (공백 포함)
키워드 밀도: 핵심 키워드 15-20회 자연스럽게 사용 (2-3% 밀도)
`;

  if (contentType === 'informational') {
    return baseInfo + `
## 정보성 글쓰기 메타프롬프트 (판매성 스타일 활용)

### 역할
당신은 10년 경력의 콘텐츠 마케터이자 전문 분야 에디터입니다. 복잡한 정보를 쉽고 재미있게 전달하며, 독자가 끝까지 읽고 실제로 활용할 수 있는 실용적인 콘텐츠를 작성하는 전문가입니다.

### 글쓰기 구조 (2000-3000자)

#### 1. 도입부 (400-500자) - 문제 제기
- 독자가 겪는 일반적인 문제/궁금증을 생생하게 묘사
- "${primaryKeyword}"와 관련된 공감 가는 상황 제시
- "~하신 적 있으신가요?"로 시작하여 독자 참여 유도
- 통계나 조사 결과로 보편성 입증
- 글을 읽으면 얻을 구체적 이익 암시
- 핵심 키워드 3-4회 자연스럽게 포함

#### 2. 현황 진단 (300-400자) - 긴급성 부여
- **※주의하세요※** 또는 **충격적 사실** 형태로 시작
- "${primaryKeyword}"에 대한 잘못된 상식이나 관행 지적
- 많은 사람들이 하는 실수 나열
- 문제를 방치했을 때의 구체적 결과 제시
- 구체적 수치로 심각성 전달
- 핵심 키워드 2-3회 포함

#### 3. 핵심 정보 제공 (800-1000자)
**"${primaryKeyword}의 진짜 비밀 O가지"** 형태로 구성
- ${subKeywords[0] || '기본 원리'}: 원리/이론을 쉬운 비유로 설명
- ${subKeywords[1] || '실용적 방법'}: 단계별 방법론과 구체적 액션
- ${subKeywords[2] || '성공 사례'}: 실제 사례/케이스 스터디
- 각 섹션마다 즉시 적용 가능한 팁 제공
- 핵심 키워드를 각 소제목과 내용에 자연스럽게 6-8회 포함

#### 4. 실전 가이드 (400-500자)
**"지금 바로 시작하는 방법"**
- [초급편] "${primaryKeyword}" 오늘부터 할 수 있는 3가지
- 구체적 행동 지침과 필요 도구/리소스
- 예상 소요 시간과 효과
- 흔한 실수와 해결책
- 핵심 키워드 2-3회 포함

#### 5. 검증과 신뢰성 (200-300자)
- 실제 적용 결과와 Before & After 비교
- "${primaryKeyword}" 관련 구체적 수치와 성과
- 전문가 의견이나 연구 결과 인용
- 핵심 키워드 1-2회 포함

#### 6. 실행 독려 (200-300자) - 정보성 CTA
- "오늘 배운 ${primaryKeyword} 방법 중 하나만 실천해보세요"
- 추가 학습 리소스나 도구 제공
- 독자 참여 유도 (댓글, 경험 공유)
- 핵심 키워드 1-2회 포함

### 필수 작성 원칙
- **볼드체**: 핵심 개념과 중요 수치 강조
- 소제목으로 스캐닝 가능한 구조 구성
- 불릿포인트로 정보 정리
- 복잡한 개념은 일상적 비유로 설명
- 전문 용어는 쉬운 말로 풀어쓰기
- 중간중간 독자에게 질문 던지기
- 실습 과제나 체크리스트 제공
`;
  } else {
    return baseInfo + `
## 판매성 글쓰기 메타프롬프트 (제품/서비스 통합)

### 역할
당신은 15년 경력의 이커머스 마케팅 전문 카피라이터입니다. 감성적 스토리텔링과 데이터 기반 설득력을 결합하여 제품과 서비스 모두에서 높은 전환율을 달성하는 판매 글쓰기 전문가입니다.

### 글쓰기 구조 (2000-3000자)

#### 1. 도입부 (400-500자) - 공감대 형성
- 타겟 고객이 "${primaryKeyword}"와 관련해 겪는 고민/아픔을 구체적으로 묘사
- 일상 속 불편함이나 비즈니스 문제를 생생하게 표현
- "~하신 적 있으신가요?", "~때문에 고민이시죠?" 활용
- 구체적 상황 제시 (시간, 장소, 감정, 손실)
- ${subKeywords[0] || '고객 고민'} 키워드로 공감대 형성
- 핵심 키워드 3-4회 자연스럽게 포함

#### 2. 문제 심화 (300-400자) - 긴급성 부여
- **※주의하세요※** 형식으로 경고 메시지
- "${primaryKeyword}" 문제를 방치 시 발생할 부정적 결과 강조
- 시간이 지날수록 악화되는 상황/손실 묘사
- 통계나 수치로 심각성 뒷받침
- 기회비용과 경쟁에서 뒤처짐 강조
- 핵심 키워드 2-3회 포함

#### 3. 차별화 포인트 (400-500자)
**"하지만 ${primaryKeyword}은 다릅니다"**
- 기존 솔루션의 한계점 지적
- 독특한 개발 스토리나 철학 소개
- ${subKeywords[1] || '차별점'} 중심의 독자적 방법론 강조
- 특허, 인증, 수상, 전문 자격증 등 공신력 제시
- 핵심 키워드 3-4회 포함

#### 4. 가치 제안 (600-700자) - 스토리텔링
**드라마틱한 스토리 전개:**
- 개발 과정의 극단적 테스트/노력 스토리
- ${subKeywords[2] || '특별함'} 중심의 차별화된 기술/서비스
- 실제 효과와 변화 사례
- 전문성과 독자적 프로세스 강조
- 성공 사례와 케이스 스터디
- 투자 대비 수익(ROI) 구체적 제시
- 핵심 키워드 4-5회 포함

#### 5. 사회적 증거 (200-300자)
- 판매량: "${primaryKeyword} OO만개 판매 돌파"
- 리뷰: "고객 만족도 OO%, OOO개의 후기"
- 성과: "평균 OO% 성장 달성, O년 연속 1위"
- 누적 고객과 대기업/유명 브랜드 실적
- 핵심 키워드 2-3회 포함

#### 6. 고객 변화/효과 (300-400자)
**"이런 변화가 생깁니다"**
- 즉각적인 체감 효과 (단기)
- 장기적 개선 효과와 지속적 혜택
- 구체적 수치와 ROI 제시
- Before & After 극적 비교
- ${subKeywords[3] || '효과'} 중심의 결과 강조
- 핵심 키워드 2-3회 포함

#### 7. 구매 유도 (200-300자) - 강력한 CTA
- 한정 혜택: "O월 한정 특가/무료 상담"
- 리스크 제거: "OO일 환불 보장/무료 체험"
- 긴급성: "선착순 OO명 한정"
- 명확한 행동 지시: "지금 바로 신청하기"
- 연락처나 링크 명시
- 핵심 키워드 1-2회 포함

### 필수 작성 원칙
- **볼드체**: 핵심 메시지와 중요 수치 강조
- 짧은 문장과 단락으로 가독성 확보
- 구어체로 친근감 조성
- 숫자와 데이터로 신뢰도 구축
- 감정 호소: 가족, 건강, 성공, 성장, 시간 절약
- 변화 전후의 구체적 상황 대비
- 미래의 긍정적 모습 시각화
- 과도한 주장이나 허위/과장 표현 절대 금지
`;
  }
}